 DATA SEGMENT
BUF	DB 9,8,7,4,5,6,3,2,12,15
count equ $-BUF
BU1 DB "THE MIN IS:",'$'
BU2 DB 0AH,0DH, "THE MAX IS:",'$'

DATA ENDS
CODE SEGMENT
ASSUME CS:CODE, DS:DATA, ES: DATA
START:MOV	AX,	DATA
	MOV	DS,	AX
	MOV CX,COUNT
	LEA SI,BUF
		MOV BH,[SI]
	MOV BL,[SI]
X1:   MOV AL,[SI]
	CALL DAXIAO   
	INC SI
	LOOP X1 
	 LEA	DX,BU1
   MOV AH,09H
	INT	21H
	MOV AL, BL
	CALL SHUCHU
	LEA	DX,BU2
   MOV AH,09H
	INT	21H
	MOV AL,BH
	CALL SHUCHU
	   
	   
	
      MOV AH,4CH
	INT	21H
DAXIAO PROC NEAR
	CMP AL,BL
	JB	X2
	CMP AL,BH
	JB X3
	MOV BH,AL 
	JMP X3
X2:   MOV BL,AL
X3:
	RET

DAXIAO ENDP 

SHUCHU PROC NEAR
     MOV DL,AL
     CMP DL,09H
     JB X4
     ADD DL,07H
X4:  ADD DL,30H
     MOV AH,02H
     INT 21H
    
    RET
SHUCHU ENDP

CODE	ENDS
	END	START