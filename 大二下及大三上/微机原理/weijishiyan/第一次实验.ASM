DATA SEGMENT
STRING1	DB	'HAPPY ASM'       ;读入字符串1
LEN		EQU	$-STRING1        ;计算字符串长度
STRING2	DB	'HAPPY ASM'		;读入字符串2
MSG1		DB	'Match!', 0DH, 0AH, '$'
MSG2		DB	'No Match!', 0DH, 0AH, '$'
DATA ENDS
CODE SEGMENT
	ASSUME CS:CODE, DS:DATA, ES: DATA
START:MOV	AX,	DATA
	MOV	DS,	AX		
	MOV	ES,	AX
	LEA	SI,	STRING1		   ;读入字符串1
	LEA	DI,	STRING2		;读入字符串2
	MOV	CX,	LEN
COMPARE:
	MOV	AL,	[SI]
	MOV	BL,	[DI]
	CMP	AL,	BL
	JNE	DISMATCH				;比较当前位
	INC	SI
	INC	DI
	DEC	CX					;判定是否比较完毕
	JNZ	COMPARE				


MATCH:LEA	DX,	MSG1
	JMP	DISP
DISMATCH:LEA	DX,	MSG2
DISP:MOV	AH,	09H				;输出
	INT	21H
	MOV	AH,	4CH			
	INT	21H
CODE	ENDS
	END	START