DATA SEGMENT
BUF	DB	30
	DB	'?'
	DB	30	DUP('?')         ;缓冲区
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE, DS:DATA, ES: DATA
START:MOV	AX,	DATA
	MOV	DS,	AX		
	LEA DX,BUF
	MOV AH,0AH		
	INT	21H				;输入字符串
	LEA SI,BUF+1
	MOV CL,[SI]
 	MOV CH,00H				;设置循环次数
	LEA SI,BUF+2
 X2: MOV AL,[SI]
	CMP AL,61H
	JB	X1				;确定边界
	CMP AL,7AH
	JA X1					;确定边界
	SUB AL,20H				;转换
X1:	MOV [SI],AL
	INC SI
	LOOP X2
LEA	DX,BUF+2
   MOV AH,09H
	INT	21H				;输出结果
      MOV AH,4CH
	INT	21H
CODE	ENDS
	END	START