LIBRARY IEEE;
USE IEEE.STD_LOGIC_1164.ALL;
USE IEEE.STD_LOGIC_ARITH.ALL;
USE IEEE.STD_LOGIC_UNSIGNED.ALL;
ENTITY CONTROM IS
PORT(
    ADDR:IN STD_LOGIC_VECTOR(5 DOWNTO 0);
    UA:OUT STD_LOGIC_VECTOR(5 DOWNTO 0);
    D:OUT STD_LOGIC_VECTOR(20 DOWNTO 0)
